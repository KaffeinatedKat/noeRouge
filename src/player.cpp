#include "player.h"

void Player::updateDirection()
{
    if (IsKeyDown(KEY_A))
    {
        direction.x = -1;
    }
    else if (IsKeyDown(KEY_D))
    {
        direction.x = 1;
    }
    if (IsKeyDown(KEY_S))
    {
        direction.y = 1;
    }
    else if (IsKeyDown(KEY_W))
    {
        direction.y = -1;
    }
}

void Player::onRender()
{
    Rectangle rectangle = getBounds();
    DrawRectangle(rectangle.x, rectangle.y, rectangle.width, rectangle.height, MAROON);
}
